height = 5.8
print(height)
print(type(height))
# 4 Boolean
# True or False 1 and 0
isMale = True
print(isMale)
print(type(isMale))
# 5 List
fruits = ["Apple", "Banana", "Mango", 135, 5.8, True]
print(fruits)
print(type(fruits))
